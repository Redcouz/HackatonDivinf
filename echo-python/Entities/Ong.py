class Ong:
    def __init__(self, name, account, image):
            self._name = name
            self._account = account
            self._image = image
    
@property
def get_name(self):
    return self._name

@get_name.setter
def set_name(self, name):
    self._name = name
    
@property
def get_account(self):
    return self._account

@get_account.setter
def get_account(self, account):
    self._account = account

@property
def get_image(self):
    return self._image

@get_image.setter
def image(self, image):
    self._image = image